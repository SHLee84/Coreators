				<h2>Our Vision</h2>
				<p>Coreators is a Toronto-based nonprofit organization established in 2015, with a vision to discover and nurture what can be called a uniquely Korean-Canadian culture.</p>
				<p>Coreators plan to fulfill such vision by building a community of creative individuals where they can find an environment to proactively nurture their creativity while garnering a supportive audience through both individual and collective production and exhibition of their creative works.</p>
				<h2>Our Mission</h2>
				<p>It is our mission to provide members of Coreators the following benefits;
					<ol>
						<li>The opportunity to connect and network with creative individuals that are either involved in their own fields of creativity or in other related fields</li>
						<li>A supportive environment that aims to foster further growth of their creative skills through various events and projects that both inspire and encourage production and,</li>
						<li>The exposure to a general audience built by the marketing of the organization as a collective as well as by the production and success of each individual members of the group</li>
					</ol>
